* Scope:

In Kotlin, the scope of a variable or function refers to the part of the code where the variable or function is accessible and can be used.
Kotlin defines various levels of scope, including:

    1: Top-Level Scope: Variables or functions defined at the top level of a file, outside of any class. They are accessible throughout the entire file.

    2: Class Scope: Variables or functions defined within a class are accessible within that class, and their visibility may be controlled using visibility
     modifiers.

    3: Local Scope: Variables defined within a function or block are only accessible within that function or block.
    *


* Visibility Modifiers:

Kotlin provides visibility modifiers that control the scope and accessibility of classes, functions, properties, and other code elements.
* The main visibility modifiers are:

        -> public: The default visibility, accessible from anywhere.
        -> internal: Accessible within the same module (a set of Kotlin files compiled together).
        -> protected: Accessible within the class and its subclasses.
        -> private: Accessible only within the same class.
